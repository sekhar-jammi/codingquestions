package com.swap;

public class StringInSwitchCase {
       public static void main(String[]args) {
    	      String fruit="ffddf";
    	      switch(fruit)
    	    	  {  
    	    	  case "Mango":
    	    		  System.out.println("Sweet");
    	    		  break;
    	    	  case "Apple":
    	    		  System.out.println("Delicious");
    	    		  break;
    	    	  case "Orange":
    	    		  System.out.println("Lusicious");
    	    		  break;
    	    	  default	:
    	    		  System.out.println("Not fruit..");
    	      }
       }
}
